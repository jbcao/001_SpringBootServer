/**
 * @Autowired
 *     为我们注入一个定义好的bean
 *
 *     是一个用于容器配置的注释
 *
 *     是自动装配之意，即将spring中bean自动和需要这个bean的类组装在一起
 *
 */



/**
 * @Autowired应用
 *
 *       一、应用于构造函数
 *
 *       二、应用于setter方法
 *
 *       三、用于具有任意名称和多个参数的方法
 *
 *       四、应用于字段，或者将其与构造函数混合（以上是常用用法、下面几种方面不太常见）
 *
 *       五、添加到该类型数组的字段或方法
 *
 *       六、添加存放某类型的set和map集合容器
 *
 *
 *
 *
 */
