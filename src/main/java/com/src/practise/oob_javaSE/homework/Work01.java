package com.src.practise.oob_javaSE.homework;

/**
 * 1.作一个加法器，计算它们的和
 * 2.作一个减法器，计算它们的差
 * 3.作一个乘法器，计算它们的积
 * 4.作一个除法器，计算它们的商(和余数)
 */
public class Work01 {
    public static void main(String[] args) {
        int i = 9;
        int j = 4;
        int k = i + j;
        int l = i - j;
        int m = j * i;
        int n = i / j;
        int o = i % j;
        System.out.println(k);
        System.out.println(l);
        System.out.println(m);
        System.out.println(n);
        System.out.println(o);
        System.out.println();
    }
}
