public class DataType {
    public static void main(String[] args) {
        /**
         * a的asvcll码和b的相加，97+96
         */
        int test1 ='a'+'b';
        System.out.println(test1);
        /**
         * 195在ascii里面对应的字符
         */
        char test2='a'+'b';
        System.out.println(test2);
        /**
         * 125所对应的字符
         */
        char test3='a'+28;
        System.out.println(test3);
        /**
         * unicode所对应的值
         */
        char test4='中';
        System.out.println(test4);
    }
}
