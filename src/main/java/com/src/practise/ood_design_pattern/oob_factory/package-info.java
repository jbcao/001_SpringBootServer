package com.src.practise.ood_design_pattern.oob_factory;
/**
 * 工厂模式
 *   实现了创建者和调用者的分离
 *
 * 分为，这三种越分越细
 *  简单工厂，不属于23种设计模式
 *  工厂方法，用得多
 *  抽象工厂，用得少，特别大项目会用到
 *
 *
 *  工厂设计模式的好处
 *
 *   最大好处：创建对象：工厂模式是最常用的实例化对象的模式，是用工厂方法替代new的一种操作
 *   利用工厂模式可以降低程序的耦合性，为后期的维护修改提供了很大的便利
 *   将选择实现类、创建对象统一管理和控制，从而将调用者跟实现类解耦
 *   工厂与容器
 *       容器主要做存放
 *       工厂主要做生产
 *
 *
 *
 */